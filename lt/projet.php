             <h3>Le constat</h3>
             <p>Les enfants sont équipés de plus en plus tôt d’un appareil numérique personnel. En Europe, 35% des jeunes âgés de 9 à 11 ans se connectent quotidiennement à Internet en utilisant un smartphone. Ce taux monte à 56% en Lituanie. (Source : EU Kids Online 2020).</p>

<p>En France, les enfants sont, en moyenne, âgés de 7 ans lors de l’acquisition de leur première tablette et de 10 ans pour le premier smartphone. (Source : La parentalité à l’épreuve du numérique OPEN/UNAF 2020).</p>

<p>Ces jeunes ont un usage essentiellement récréatif de leurs outils numériques : s’ils passent beaucoup de temps à regarder des vidéos sur leur smartphone, ils maintiennent le lien social en jouant en ligne ou en communiquant sur les réseaux sociaux.</p>

<p>Lorsqu’ils sont confrontés à du contenu inapproprié ou violent, 55% des jeunes enfants affirment savoir réagir dans ce genre de situation soit en :</p>
<ul>
    <li>Menant des actions individuelles. 36% peuvent simplement fermer l’application ou bien bloquer la personne concernée (34%) ou ne rien faire en espérant que la situation se tasse (36%).</li>
    <li>Prévenant une personne ressource. Les parents (40% des répondants) ou les amis (50% des cas).</li>
</ul>

<p>Beaucoup ont également été victimes de violence en ligne ou confrontés à du contenu inapproprié.</p>

<p>Au final, si 80% des enfants de 9/11 ans déclarent aller bien dans leur vie numérique, 51% trouvent plus facile d’être soi-même en ligne (69% en Lituanie). 38% cloisonnent les différents espaces et ne parlent pas des mêmes choses en ligne et en face à face.</p>

<p>On constate donc une difficulté dans l’appropriation et la gestion quotidienne des outils numériques dans la vie des enfants et pré-adolescents, due à un manque d’accompagnement de la part des adultes, soit dans leur vie scolaire (par les enseignants à l’école), soit dans leur vie familiale (par leurs parents).</p>
