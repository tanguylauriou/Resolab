         <h3>Proposition</h3>
             <p>Un effort doit être fait sur l’acquisition de compétences numériques au-delà du simple usage des outils que les jeunes maîtrisent très bien la plupart du temps et davantage sur les compétences informelles et non formelles de comportements dans ces espaces (numériques) de vie.</p>

<p>L’Union Européenne travaille depuis 2013 sur un programme commun de développement des compétences numériques The Digital Competence Framework for Citizens (DigComp). Il sert de base à l’ensemble des programmes scolaires dans les pays de l’UE. Mais qu’en est-il vraiment de l’acquisition des compétences clés des jeunes en matière d’usages numériques et de leur véritable gestion des usages ?</p>

<p>RESOLAB propose une méthodologie nouvelle pour étudier et comprendre les pratiques numériques réelles des enfants, les faire tester dans un milieu numérique sécurisé et sécurisant leurs premiers usages, puis leur faire prendre conscience des enjeux de cette vie numérique en associant les enseignants et les parents. Cette méthodologie éprouvée sur 4 pays de l’Europe, aux usages chez les jeunes très différenciés, va permettre la montée en compétence numérique en s’appuyant sur le référentiel DigComp.</p>
