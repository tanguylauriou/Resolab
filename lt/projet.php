         <div class="left-column">
             <h3>Objectifs</h3>
              <p>L’objectif du projet est de développer des ressources numériques adaptées aux enfants, aux enseignants et aux familles afin de promouvoir les compétences numériques conformément au cadre européen DigComp.</p>

        <p>Pour cela, 3 OBJECTIFS STRATÉGIQUES correspondants aux 3 publics concernés par ce dispositif sont déclinés :</p>
        <ol>
            <li>Former les enfants à l'utilisation des appareils numériques par l'expérimentation.</li>
            <li>Fournir aux enseignants des outils pour travailler avec DigComp en classe.</li>
            <li>Fournir aux familles des ressources pour développer les compétences numériques à la maison.</li>
        </ol>

        <p>Concrètement, nous allons aboutir à la création d’un espace en ligne pour que les enfants puissent monter en compétence en matière d’usages numériques par l’expérimentation : une plateforme européenne de type réseau social accessible uniquement aux élèves de chaque classe pour tester la sociabilité en ligne.</p>

        <p>La construction de cet outil s’appuiera sur une enquête et des focus groupes afin de comprendre les usages réels des enfants.</p>

        <p>Puis, nous allons élaborer des mises en situations réelles telles que les enfants peuvent en vivre sur le numérique, permettant d’aborder les compétences relatives à DigComp.</p>

        <p>En parallèle, nous allons sonder et échanger avec les enseignants pour d’une part avoir une vision globale de leurs représentations des pratiques numériques des élèves, et d’autre part, comprendre les freins ou points forts pour la mise en application des compétences numériques à enseigner dans le cadre de la programmation scolaire.</p>

        <p>Enfin, pour un accompagnement complet, il est fondamental d’associer les parents qui auront une place importante dans ce dispositif. Concrètement, nous allons leur proposer un journal de bord pour comprendre les points de blocage et les éléments facilitants au sein de la famille. Nous allons également nous attarder sur les représentations des familles en matière de pratiques numériques des enfants au travers d’un questionnaire.</p>

        <p>Tout cela va nous permettre d’élaborer un théâtre forum qui réunira l’ensemble de ces publics afin de prendre en compte l’ensemble des paramètres pour accompagner au mieux les enfants dans le cadre du programme DigComp. Un guide en ligne à destination de chacun de ces publics viendra compléter le dispositif.</p>

        <p>De plus, nous souhaitons soutenir les enseignants, les chefs d'établissement et les autres professions de l'enseignement en permettant à ces derniers de comprendre les usages réels afin de les accompagner tout en leur proposant un outil pour appliquer le programme scolaire.</p>

        <p>Nous incluons dans cet axe là les parents, acteur majeur pour la montée en compétence des enfants.</p>
